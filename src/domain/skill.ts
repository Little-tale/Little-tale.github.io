// 이 파일은 기술 스택 섹션에서 카테고리별 스킬 묶음과 AI 워크플로 툴을 표현하는 타입을 정의한다.
// 한 카테고리 안에 여러 기술명이 들어가는 단순 구조지만, 섹션 렌더링의 기준 타입이 된다.
// 스킬 카테고리 한 묶음의 이름과 항목 목록이다.
export type SkillCategory = {
  name: string;
  items: readonly string[];
};

// 포트폴리오에서 "내가 어떻게 AI를 쓰는가" 를 드러내는 도구 한 개의 카드 구조다.
// 단순 로고 나열이 아니라 역할 설명을 함께 두어 오케스트레이션 감각을 보여 주는 것이 목적이다.
export type AIWorkflowTool = {
  /** 카드 상단에 굵게 노출되는 브랜드/툴 이름. */
  name: string;
  /** 괄호 주석처럼 병기되는 보조 라벨. 공식 패키지명이나 별칭을 담는다. */
  alias?: string;
  /** 제공 주체(회사·조직). 카드 하단의 메타 라인에 노출된다. */
  vendor: string;
  /** 카드의 핵심 한 줄 설명. 역할 분담이 한눈에 보이도록 짧게 쓴다. */
  role: string;
};
